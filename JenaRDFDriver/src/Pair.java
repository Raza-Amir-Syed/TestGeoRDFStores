

public class Pair {
	
	private long time;
	private long count;
	
	
	public Pair(long time, long count) {
		super();
		this.time = time;
		this.count = count;
	}
	public long getTime() {
		return time;
	}
	public void setTime(long time) {
		this.time = time;
	}
	public long getCount() {
		return count;
	}
	public void setCount(long count) {
		this.count = count;
	}
	
}
